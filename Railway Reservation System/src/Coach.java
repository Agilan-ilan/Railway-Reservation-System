
public class Coach {
	
	private String coachName;
	private String coachType;
	private int coachCapacity;
	private int seatReservationCount;
	private boolean isAC;
	
	
	public String getCoachName() {
		return coachName;
	}
	public void setCoachName(String coachName) {
		this.coachName = coachName;
	}
	public String getCoachType() {
		return coachType;
	}
	public void setCoachType(String coachType) {
		this.coachType = coachType;
	}
	public int getCoachCapacity() {
		return coachCapacity;
	}
	public void setCoachCapacity(int coachCapacity) {
		this.coachCapacity = coachCapacity;
	}
	public int getSeatReservationCount() {
		return seatReservationCount;
	}
	public void setSeatReservationCount(int seatReservationCount) {
		this.seatReservationCount = seatReservationCount;
	}
	public boolean isAC() {
		return isAC;
	}
	public void setAC(boolean isAC) {
		this.isAC = isAC;
	}
	
	
}
